PhysicsList::PhysicsList() : G4VModularPhysicsList()
{
  fMessenger = new PhysicsListMessenger(this);

  SetVerboseLevel(1);

  // EM physics
  fEmPhysicsList = new PhysListEmStandard(fEmName = "local");

  // Em options
  //
  G4EmParameters::Instance()->SetBuildCSDARange(true);

  SetDefaultCutValue(100*nm);
  G4ProductionCutsTable::GetProductionCutsTable()->SetEnergyRange(10*eV, 1*GeV);
  G4EmParameters* param = G4EmParameters::Instance();
  param->SetMinEnergy(1*eV);
  param->SetMaxEnergy(1*GeV);
}

